"""
Dataset wrapping Capytaine BEM output for Module 2 (DeepONet) training.

Expects NetCDF files generated by scripts/generate_bem_data.py
with variables: added_mass, radiation_damping, excitation_force, omega.
"""
from __future__ import annotations
from pathlib import Path
from typing import Dict, List, Optional, Tuple
import numpy as np
import torch
from torch.utils.data import Dataset


class CapytaineDataset(Dataset):
    """
    Torch Dataset of BEM responses from Capytaine solver runs.

    Each sample:
        branch_input : (7,)  device properties [radius, draft, mass, vol, Bpto, cog, depth]
        omega        : (N_omega,)
        added_mass   : (N_omega,)
        rad_damping  : (N_omega,)
        exc_force    : (N_omega,)
        rao          : (N_omega,)
    """

    BRANCH_KEYS = ["radius", "draft", "mass", "volume", "Bpto", "cog", "depth"]

    def __init__(self, data_dir: str, n_omega: int = 64,
                 omega_range: Tuple = (0.2, 3.0),
                 normalise: bool = True):
        self.data_dir = Path(data_dir)
        self.n_omega  = n_omega
        self.omega    = np.linspace(*omega_range, n_omega)
        self.files    = sorted(self.data_dir.glob("*.nc"))
        self.normalise = normalise
        self._stats: Optional[Dict] = None
        if not self.files:
            raise FileNotFoundError(f"No .nc files found in {data_dir}")

    def __len__(self): return len(self.files)

    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        try:
            import xarray as xr
            ds = xr.open_dataset(self.files[idx])
        except ImportError:
            raise ImportError("pip install xarray netCDF4 to use CapytaineDataset")

        # Interpolate to fixed omega grid
        def interp(arr, src_omega):
            return np.interp(self.omega, src_omega, arr)

        omega_src = ds["omega"].values
        branch    = np.array([float(ds.attrs.get(k, 0.0))
                               for k in self.BRANCH_KEYS], dtype=np.float32)
        sample    = {
            "branch_input":       torch.from_numpy(branch),
            "omega":              torch.from_numpy(self.omega.astype(np.float32)),
            "added_mass":         torch.from_numpy(interp(ds["added_mass"].values, omega_src).astype(np.float32)),
            "radiation_damping":  torch.from_numpy(interp(ds["radiation_damping"].values, omega_src).astype(np.float32)),
            "excitation_force":   torch.from_numpy(interp(np.abs(ds["excitation_force"].values), omega_src).astype(np.float32)),
            "rao":                torch.from_numpy(interp(ds["rao"].values, omega_src).astype(np.float32)),
        }
        ds.close()
        return sample


class CapytaineSampler:
    """
    Runs Capytaine BEM for a batch of device configurations.
    Generates training data for Module 2.
    """

    def __init__(self, omega: np.ndarray):
        self.omega = omega

    def run_single(self, props: Dict, output_path: str) -> bool:
        """Run Capytaine for one device config and save to NetCDF."""
        try:
            import capytaine as cpt
            import xarray as xr
        except ImportError:
            raise ImportError("pip install capytaine to generate BEM data")

        r = float(props.get("radius", 3.0))
        d = float(props.get("draft",  3.0))
        h = float(props.get("depth",  50.0))

        # Build mesh
        sphere = cpt.mesh_vertical_cylinder(radius=r, length=d,
                                             center=(0, 0, -d/2), name="wec")
        body   = cpt.FloatingBody(sphere, name="wec")
        body.add_all_rigid_body_dofs()

        # Set up problems
        problems = [
            cpt.RadiationProblem(body=body, radiating_dof="Heave",
                                  omega=w, sea_bottom=-h)
            for w in self.omega
        ] + [
            cpt.DiffractionProblem(body=body, omega=w, sea_bottom=-h)
            for w in self.omega
        ]

        # Solve
        solver  = cpt.BEMSolver()
        results = solver.solve_all(problems, keep_details=False)
        ds      = cpt.assemble_dataset(results)

        # Add device props as metadata
        ds.attrs.update(props)
        ds.to_netcdf(output_path)
        return True
