"""RED tests for Phase 4 P4-T4: FarmDataset synthetic farm configurations.

Defines the contract for FarmDataset before any implementation exists.
All tests must FAIL on first run (ModuleNotFoundError expected).

Physical context
----------------
A synthetic WEC farm dataset samples farm configurations via Latin
Hypercube Sampling (LHS) over:
    - N         : number of WECs in [N_min, N_max]
    - radius    : WEC radius (m)
    - draft     : WEC draft (m)
    - depth     : water depth (m)
    - Bpto      : PTO damping (N.s/m)
    - spacing   : minimum inter-device spacing (m), drawn from [d_min, d_max]
    - heading   : dominant wave heading (rad), drawn from [0, 2*pi]

Positions are generated by placing N WECs on a regular grid with the
sampled spacing, then rotating by the heading angle. This gives
physically plausible layouts without requiring a numerical solver.

Dataset contract
----------------
FarmDataset(n_cases, n_min, n_max, seed) exposes:
    len(ds)              -> n_cases
    ds[i]                -> FarmSample(props, pos, omega, A_base, B_base)

FarmSample fields:
    props  : (N, 4)     WEC property vectors
    pos    : (N, 2)     WEC positions (metres)
    omega  : (K,)       frequency query points
    A_base : (N, 6, 6)  baseline added-mass (LinearSuperpositionBaseline)
    B_base : (N, 6, 6)  baseline radiation-damping

All shapes must be consistent within a sample.
N can differ between samples (variable-length farm).
"""
from __future__ import annotations

import numpy as np
import torch
import pytest

from nossomar.data.farm_dataset import FarmDataset, FarmSample


# ---------------------------------------------------------------------------
# Fixtures
# ---------------------------------------------------------------------------

@pytest.fixture(scope="module")
def dataset():
    return FarmDataset(n_cases=50, n_min=2, n_max=6, seed=42)


# ---------------------------------------------------------------------------
# Construction
# ---------------------------------------------------------------------------

def test_dataset_instantiation(dataset):
    assert dataset is not None


def test_dataset_length(dataset):
    assert len(dataset) == 50


# ---------------------------------------------------------------------------
# Sample structure
# ---------------------------------------------------------------------------

def test_sample_is_farmsample(dataset):
    s = dataset[0]
    assert isinstance(s, FarmSample)


def test_sample_has_all_fields(dataset):
    s = dataset[0]
    assert hasattr(s, "props")
    assert hasattr(s, "pos")
    assert hasattr(s, "omega")
    assert hasattr(s, "A_base")
    assert hasattr(s, "B_base")


# ---------------------------------------------------------------------------
# Shape consistency
# ---------------------------------------------------------------------------

@pytest.mark.parametrize("idx", [0, 1, 10, 25, 49])
def test_sample_shapes_consistent(dataset, idx):
    s = dataset[idx]
    N = s.props.shape[0]
    assert s.props.shape == (N, 4)
    assert s.pos.shape   == (N, 2)
    assert s.A_base.shape == (N, 6, 6)
    assert s.B_base.shape == (N, 6, 6)
    assert s.omega.ndim == 1


# ---------------------------------------------------------------------------
# N variability
# ---------------------------------------------------------------------------

def test_variable_n_across_samples(dataset):
    """Different samples should have different N (LHS over N_min..N_max)."""
    ns = {dataset[i].props.shape[0] for i in range(50)}
    assert len(ns) > 1, f"All samples have the same N={ns}"


def test_n_within_bounds(dataset):
    for i in range(50):
        N = dataset[i].props.shape[0]
        assert 2 <= N <= 6, f"Sample {i} has N={N} outside [2, 6]"


# ---------------------------------------------------------------------------
# Physical constraints on baseline outputs
# ---------------------------------------------------------------------------

@pytest.mark.parametrize("idx", [0, 5, 20])
def test_no_nan_in_sample(dataset, idx):
    s = dataset[idx]
    assert not torch.isnan(s.A_base).any()
    assert not torch.isnan(s.B_base).any()
    assert not torch.isnan(s.props).any()
    assert not torch.isnan(s.pos).any()


@pytest.mark.parametrize("idx", [0, 7, 33])
def test_A_base_symmetric(dataset, idx):
    A = dataset[idx].A_base
    diff = (A - A.transpose(-2, -1)).abs().max()
    assert diff < 1e-4


@pytest.mark.parametrize("idx", [0, 7, 33])
def test_B_base_symmetric(dataset, idx):
    B = dataset[idx].B_base
    diff = (B - B.transpose(-2, -1)).abs().max()
    assert diff < 1e-4


# ---------------------------------------------------------------------------
# Positions: minimum spacing respected
# ---------------------------------------------------------------------------

def test_minimum_spacing(dataset):
    """No two WECs should be closer than 10 m in any sample."""
    for i in range(50):
        pos = dataset[i].pos   # (N, 2)
        N = pos.shape[0]
        if N < 2:
            continue
        for a in range(N):
            for b in range(a + 1, N):
                dist = (pos[a] - pos[b]).norm().item()
                assert dist >= 9.9, f"Sample {i} devices {a},{b} too close: {dist:.1f} m"


# ---------------------------------------------------------------------------
# Reproducibility
# ---------------------------------------------------------------------------

def test_reproducible_with_same_seed():
    ds1 = FarmDataset(n_cases=10, n_min=2, n_max=4, seed=0)
    ds2 = FarmDataset(n_cases=10, n_min=2, n_max=4, seed=0)
    for i in range(10):
        assert torch.allclose(ds1[i].props, ds2[i].props, atol=1e-6)
        assert torch.allclose(ds1[i].pos,   ds2[i].pos,   atol=1e-6)


def test_different_seeds_differ():
    ds1 = FarmDataset(n_cases=10, n_min=2, n_max=4, seed=0)
    ds2 = FarmDataset(n_cases=10, n_min=2, n_max=4, seed=99)
    any_diff = any(
        not torch.allclose(ds1[i].props, ds2[i].props) for i in range(10)
    )
    assert any_diff
